    private void openMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openMenuItemActionPerformed
       
        JInternalFrame frame = new JInternalFrame("nodes.xml",true,true,true);
        frame.setSize(200,200);
        TemplateLibraryPanel panel = new TemplateLibraryPanel();

        Path templateFile = PathUtil.createUserDirPath("/nodes/shaders/nodes.xml");
        if (Files.exists(templateFile)) {
            NodeTemplateLoader loader = new NodeTemplateLoader(templateFile);
            NodeTemplateLibrary library = loader.load();
            panel.setModel(library);
        }
        frame.getContentPane().add(panel);
        frame.setVisible(true);

        desktopPane.add(frame);
        //desktopPane.add(panel);
    }//GEN-LAST:event_openMenuItemActionPerformed

